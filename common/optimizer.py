class Nesterov:

    """Nesterov's Accelerated Gradient (http://arxiv.org/abs/1212.0901)"""

    def __init__(self, lr=0.01, momentum=0.9):
        self.lr = lr
        self.momentum = momentum
        self.v = None
        
    def update(self, params, grads):
        if self.v is None:
            self.v = {}
            for key, val in params.items():
                self.v[key] = np.zeros_like(val)
            
        for key in params.keys():
            self.v[key] *= self.momentum
            self.v[key] -= self.lr * grads[key]
            params[key] += self.momentum * self.momentum * self.v[key]
            params[key] -= (1 + self.momentum) * self.lr * grads[key]



class kNesterov:

    """k部nestrov 动量计算同momentum 梯度计算经过多次套娃"""

    def __init__(self, lr=0.01, momentum=0.9,k=1):
        self.lr = lr
        self.momentum = momentum
        self.v = None
        self.k = k 
        
    def update(self, params, grads):
        if self.v is None:
            self.v = {}
            for key, val in params.items():
                self.v[key] = np.zeros_like(val)
            
        for key in params.keys():
            self.v[key] *= self.momentum
            self.v[key] -= self.lr * grads[key]

            temp = self.momentum*self.v[key] - self.lr*grads[key]
            for _ in range(self.k-1):
                temp = self.momentum*temp - self.lr*grads[key]
            params[key] += temp
            # params[key] += self.momentum * self.momentum * self.v[key]
            # params[key] -= (1 + self.momentum) * self.lr * grads[key]







class AdaGrad:

    """AdaGrad"""

    def __init__(self, lr=0.01):
        self.lr = lr
        self.h = None
        
    def update(self, params, grads):
        if self.h is None:
            self.h = {}
            for key, val in params.items():
                self.h[key] = np.zeros_like(val)
            
        for key in params.keys():
            self.h[key] += grads[key] * grads[key]
            params[key] -= self.lr * grads[key] / (np.sqrt(self.h[key]) + 1e-7)


class RMSprop:

    """RMSprop"""

    def __init__(self, lr=0.01, decay_rate = 0.99):
        self.lr = lr
        self.decay_rate = decay_rate
        self.h = None
        
    def update(self, params, grads):
        if self.h is None:
            self.h = {}
            for key, val in params.items():
                self.h[key] = np.zeros_like(val)
            
        for key in params.keys():
            self.h[key] *= self.decay_rate
            self.h[key] += (1 - self.decay_rate) * grads[key] * grads[key]
            params[key] -= self.lr * grads[key] / (np.sqrt(self.h[key]) + 1e-7)


class Adadelta:
    def __init__(self,rho=0.95,eta=1e-5):
        self.rho = rho 
        self.eta = eta
        self.Ex = None
        self.Eg = None
    
    def update(self,params,grads):
        if self.Ex is None:
            self.Ex,self.Eg = {},{}
            for k,v in params.items():
                self.Ex[k] = np.zeros_like(v)
                self.Eg[k] = np.zeros_like(v)
        
        for key in params.keys():
            self.Eg[key] = self.rho*self.Eg[key] + (1-self.rho)*grads[key]**2
            delta_x = -np.sqrt(self.Ex[key]+self.eta)* grads[key]/np.sqrt(self.Eg[key]+self.eta) 
            self.Ex[key] = self.rho*self.Ex[key] + (1-self.rho)*delta_x**2
            params[key] += delta_x 







